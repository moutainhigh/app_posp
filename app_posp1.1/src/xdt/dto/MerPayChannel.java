package xdt.dto;

public class MerPayChannel {

	private String accountPay;
	private String baiduPay;
	private String weixinPay;
	private String zhifubaoPay;
	private String shuakaPay;
	private String ydhbPay;

	public String getAccountPay() {
		return accountPay;
	}

	public void setAccountPay(String accountPay) {
		this.accountPay = accountPay;
	}

	public String getBaiduPay() {
		return baiduPay;
	}

	public void setBaiduPay(String baiduPay) {
		this.baiduPay = baiduPay;
	}

	public String getWeixinPay() {
		return weixinPay;
	}

	public void setWeixinPay(String weixinPay) {
		this.weixinPay = weixinPay;
	}

	public String getZhifubaoPay() {
		return zhifubaoPay;
	}

	public void setZhifubaoPay(String zhifubaoPay) {
		this.zhifubaoPay = zhifubaoPay;
	}

	public String getShuakaPay() {
		return shuakaPay;
	}

	public void setShuakaPay(String shuakaPay) {
		this.shuakaPay = shuakaPay;
	}

	public String getYdhbPay() {
		return ydhbPay;
	}

	public void setYdhbPay(String ydhbPay) {
		this.ydhbPay = ydhbPay;
	}

}
